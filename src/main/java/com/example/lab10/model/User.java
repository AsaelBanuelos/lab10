package com.example.lab10.model;

import jakarta.persistence.*;

/**
 * JPA Entity representing a user account in the database.
 * Maps to the "users" table and stores authentication/authorization data.
 *
 * Security features:
 * - Passwords are stored using BCrypt hashing (never plain text)
 * - Email addresses are unique to prevent duplicate accounts
 * - Role-based authorization (ROLE_USER, ROLE_ADMIN)
 *
 * Database constraints:
 * - Unique constraints on username and email
 * - NOT NULL constraints on all fields
 */
@Entity
@Table(name = "users")
public class User {

    /**
     * Primary key for the user.
     * Automatically generated by the database using auto-increment.
     */
    @Id
    @GeneratedValue(strategy = GenerationType.IDENTITY)
    private Integer id;

    /**
     * Username used by Spring Security for authentication.
     * In this application, the email is stored as the username.
     * Must be unique across all users.
     */
    @Column(nullable = false, unique = true)
    private String username;

    /**
     * User's email address.
     * Must be unique and is used for login.
     */
    @Column(nullable = false, unique = true)
    private String email;

    /**
     * User's password hash.
     * Stored encrypted using BCrypt algorithm.
     * NEVER store plain text passwords.
     */
    @Column(nullable = false)
    private String password;

    /**
     * User's role for authorization.
     * Default is "ROLE_USER", can also be "ROLE_ADMIN".
     * Used by Spring Security to control access to endpoints.
     */
    @Column(nullable = false)
    private String role = "ROLE_USER";

    /**
     * Default no-argument constructor.
     * Required by JPA for entity instantiation.
     */
    public User() {
    }

    /**
     * Constructor for creating a new user with all fields.
     *
     * @param username The username (typically the email)
     * @param email The user's email address
     * @param password The BCrypt-hashed password
     * @param role The user's role (ROLE_USER or ROLE_ADMIN)
     */
    public User(String username, String email, String password, String role) {
        this.username = username;
        this.email = email;
        this.password = password;
        this.role = role;
    }

    /**
     * Gets the user ID.
     *
     * @return The database-generated user ID
     */
    public Integer getId() {
        return id;
    }

    /**
     * Gets the username.
     *
     * @return The username (email)
     */
    public String getUsername() {
        return username;
    }

    /**
     * Sets the username.
     *
     * @param username The new username
     */
    public void setUsername(String username) {
        this.username = username;
    }

    /**
     * Gets the email address.
     *
     * @return The user's email
     */
    public String getEmail() {
        return email;
    }

    /**
     * Sets the email address.
     *
     * @param email The new email address
     */
    public void setEmail(String email) {
        this.email = email;
    }

    /**
     * Gets the password hash.
     *
     * @return The BCrypt-hashed password
     */
    public String getPassword() {
        return password;
    }

    /**
     * Sets the password hash.
     * Should only be set with a properly hashed password.
     *
     * @param password The BCrypt-hashed password
     */
    public void setPassword(String password) {
        this.password = password;
    }

    /**
     * Gets the user's role.
     *
     * @return The role (ROLE_USER or ROLE_ADMIN)
     */
    public String getRole() {
        return role;
    }

    /**
     * Sets the user's role.
     *
     * @param role The new role
     */
    public void setRole(String role) {
        this.role = role;
    }
}
